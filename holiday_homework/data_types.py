
# Integer
age = 12

# Float
height = 5.5

# String
name = "kiyaan"

# Boolean
is_student = True

# List
fruits = ["apple", "banana", "cherry"]

# Tuple
coordinates = (10.0, 20.0)

# Set
unique_numbers = {1, 2, 3, 4}

# Dictionary
person = {
    "name": "kiyaan",
    "age": 12,
    "is_student": True
}

